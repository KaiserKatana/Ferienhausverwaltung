    private void bearbeitenBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bearbeitenBTNActionPerformed
        ListIterator einIterator = aenderungen.listIterator();
        KundenVerwaltung eineVerwaltung = new KundenVerwaltung();
        int kNr, spaltenNr;
        String nachname,vorname,gDatum,strasse,plz,ort,telNr;
        
        while(einIterator.hasNext())
        {
            spaltenNr = Integer.parseInt(einIterator.next().toString());
            if(spaltenNr!=-1)
            {
            kNr = Integer.parseInt(kundenTBL.getValueAt(spaltenNr, 0).toString());
            nachname = kundenTBL.getValueAt(spaltenNr, 1).toString();
            vorname = kundenTBL.getValueAt(spaltenNr, 2).toString();
            gDatum = kundenTBL.getValueAt(spaltenNr, 3).toString();
            strasse = kundenTBL.getValueAt(spaltenNr, 4).toString();
            plz = kundenTBL.getValueAt(spaltenNr, 5).toString();
            ort = kundenTBL.getValueAt(spaltenNr, 6).toString();
            telNr = kundenTBL.getValueAt(spaltenNr, 7).toString();
            eineVerwaltung.update(kNr,nachname,vorname,gDatum,strasse,plz,ort,telNr);
            }
        }
    }//GEN-LAST:event_bearbeitenBTNActionPerformed

    private void kundenTBLPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_kundenTBLPropertyChange
        int spaltenNrTMP;
        boolean isVorhanden = false;
        try
        {   spaltenNrTMP = kundenTBL.getSelectedRow();
            
            ListIterator einIterator = aenderungen.listIterator();
            while(einIterator.hasNext())
            {
                if(spaltenNrTMP == Integer.parseInt(einIterator.next().toString()))
                {
                    isVorhanden = true;
                }
            }
            
            if(!isVorhanden)
            {
                aenderungen.add(spaltenNrTMP);
            }
        }
        catch(Exception e){}
    }//GEN-LAST:event_kundenTBLPropertyChange

