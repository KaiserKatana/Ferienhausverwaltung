    public void suchen()
    {
        KundenVerwaltung eineVerwaltung = new KundenVerwaltung();
        LinkedList eineListe = null;
       
        if(gruppeComboBox.getSelectedIndex()==0)
        {
            eineListe = eineVerwaltung.SuchenKnr(Integer.parseInt(begriffText.getText()));
        }
        if(gruppeComboBox.getSelectedIndex()==1)
        {
            eineListe = eineVerwaltung.SuchenName(begriffText.getText());
        }

        ListIterator einIterator = eineListe.listIterator();
        int i=0;
        
        // <editor-fold defaultstate="collapsed" desc=" Tabellen Modell ">
        String[] titel = {"Kunden Nr", "Nachname", "Vorname", "Geburtsdatum", "Strasse", "PLZ", "Ort", "Telefon Nr"};
        boolean[] canEdit = {false, true, true, true, true, true, true, true};
        Class[] types = {Integer.class, String.class, String.class, String.class, String.class, String.class, String.class, String.class};
        
        TabellenModell einModell = new TabellenModell(eineListe.size(), titel, canEdit, types);
        kundenTBL.setModel(einModell);
        // </editor-fold>    
        
        while(einIterator.hasNext())
        {
            Kunde einKunde = (Kunde)einIterator.next();
            kundenTBL.setValueAt(einKunde.getKundenNR(),i,0);
            kundenTBL.setValueAt(einKunde.getNachname(), i, 1);
            kundenTBL.setValueAt(einKunde.getVorname(), i, 2);
            kundenTBL.setValueAt(einKunde.getGDate(), i, 3);
            kundenTBL.setValueAt(einKunde.getStrasse(), i, 4);
            kundenTBL.setValueAt(einKunde.getPLZ(), i, 5);
            kundenTBL.setValueAt(einKunde.getOrt(), i, 6);
            kundenTBL.setValueAt(einKunde.getTelefonNr(), i, 7);
            i++;
        }
    }
    
