    private void mitarbeiterTBLPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_mitarbeiterTBLPropertyChange
        int spaltenNrTMP;
        boolean isVorhanden = false;
        try
        {   spaltenNrTMP = mitarbeiterTBL.getSelectedRow();
            
            ListIterator einIterator = aenderungen.listIterator();
            while(einIterator.hasNext())
            {
                if(spaltenNrTMP == Integer.parseInt(einIterator.next().toString()))
                {
                    isVorhanden = true;
                }
            }
            
            if(!isVorhanden)
            {
                aenderungen.add(spaltenNrTMP);
            }
        }
        catch(Exception e){}
    }//GEN-LAST:event_mitarbeiterTBLPropertyChange

    private void bearbeitenBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bearbeitenBTNActionPerformed
        ListIterator einIterator = aenderungen.listIterator();
        MitarbeiterVerwaltung eineVerwaltung = new MitarbeiterVerwaltung();
        int pNr, spaltenNr;
        String nachname,vorname,gDatum,strasse,plz,ort,telNr;
        boolean taetig;
        
        while(einIterator.hasNext())
        {
            spaltenNr = Integer.parseInt(einIterator.next().toString());
            if(spaltenNr!=-1)
            {
            pNr = Integer.parseInt(mitarbeiterTBL.getValueAt(spaltenNr, 0).toString());
            nachname = mitarbeiterTBL.getValueAt(spaltenNr, 1).toString();
            vorname = mitarbeiterTBL.getValueAt(spaltenNr, 2).toString();
            gDatum = mitarbeiterTBL.getValueAt(spaltenNr, 3).toString();
            strasse = mitarbeiterTBL.getValueAt(spaltenNr, 4).toString();
            plz = mitarbeiterTBL.getValueAt(spaltenNr, 5).toString();
            ort = mitarbeiterTBL.getValueAt(spaltenNr, 6).toString();
            telNr = mitarbeiterTBL.getValueAt(spaltenNr, 7).toString();
            taetig = Boolean.parseBoolean(mitarbeiterTBL.getValueAt(spaltenNr, 9).toString());
            eineVerwaltung.update(pNr,nachname,vorname,gDatum,strasse,plz,ort,telNr,taetig);
            }
        }
    }//GEN-LAST:event_bearbeitenBTNActionPerformed

