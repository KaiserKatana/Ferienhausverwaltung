    public void suchen()
    {
        MitarbeiterVerwaltung eineVerwaltung = new MitarbeiterVerwaltung();
        LinkedList eineListe = null;
       
        if(gruppeComboBox.getSelectedIndex()==0)
        {
            eineListe = eineVerwaltung.SuchenPnr(Integer.parseInt(begriffText.getText()));
        }
        if(gruppeComboBox.getSelectedIndex()==1)
        {
            eineListe = eineVerwaltung.SuchenName(begriffText.getText());
        }

        ListIterator einIterator = eineListe.listIterator();
        int i=0;
        
       // <editor-fold defaultstate="collapsed" desc=" Tabellen Modell ">
        String[] titel = {"Personal Nr", "Nachname", "Vorname", "Geburtsdatum", "Strasse", "PLZ", "Ort", "Telefon Nr", "Aufgabenbereich", "Ist T�tig"};
        boolean[] canEdit = {false, true, true, true, true, true, true, true, true, true};
        Class[] types = {Integer.class, String.class, String.class, String.class, String.class, String.class, String.class, String.class, String.class, Boolean.class};
        
        TabellenModell einModell = new TabellenModell(eineListe.size(), titel, canEdit, types);
        mitarbeiterTBL.setModel(einModell);
        // </editor-fold>  
        
        while(einIterator.hasNext())
        {
            Mitarbeiter einMitarbeiter = (Mitarbeiter)einIterator.next();
            mitarbeiterTBL.setValueAt(einMitarbeiter.getPersonalNR(),i,0);
            mitarbeiterTBL.setValueAt(einMitarbeiter.getNachname(),i,1);
            mitarbeiterTBL.setValueAt(einMitarbeiter.getVorname(),i,2);
            mitarbeiterTBL.setValueAt(einMitarbeiter.getGDate(),i,3);
            mitarbeiterTBL.setValueAt(einMitarbeiter.getStrasse(),i,4);
            mitarbeiterTBL.setValueAt(einMitarbeiter.getPLZ(),i,5);
            mitarbeiterTBL.setValueAt(einMitarbeiter.getOrt(),i,6);
            mitarbeiterTBL.setValueAt(einMitarbeiter.getTelefonNr(),i,7);
            mitarbeiterTBL.setValueAt(einMitarbeiter.getIsTaetig(),i,9);
            i++;
        }
    }
    
    
