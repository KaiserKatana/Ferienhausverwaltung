    public void suchen()
    {
        RechnungsVerwaltung eineVerwaltung = new RechnungsVerwaltung();
        LinkedList eineListe = null;
        if(gruppeComboBox.getSelectedIndex()==0)
        {
            eineListe = eineVerwaltung.SuchenRechnung(Integer.parseInt(begriffText.getText()));
        }
        if(gruppeComboBox.getSelectedIndex()==1)
        {
            eineListe = eineVerwaltung.SuchenKnr(Integer.parseInt(begriffText.getText()));
        }
        if(gruppeComboBox.getSelectedIndex()==2)
        {
            eineListe = eineVerwaltung.SuchenReservierung(Integer.parseInt(begriffText.getText()));
        }
        if(gruppeComboBox.getSelectedIndex()==3)
        {
            eineListe = eineVerwaltung.SuchenOffen();
        }
        if(gruppeComboBox.getSelectedIndex()==4)
        {
            eineListe = eineVerwaltung.SuchenEndabnahme();
        }
        
        ListIterator einIterator = eineListe.listIterator();
        int i=0;
        
        // <editor-fold defaultstate="collapsed" desc=" Tabellen Modell ">
        String[] titel = {"Rechnungs Nr", "Kunden Nr", "Reservierungs Nr", "Mahnungen", "Offen", "Enabnahme", "Rechnungsbetrag"};
        boolean[] canEdit = {false, false, false, false, true, true, false};
        Class[] types = {Integer.class, Integer.class, Integer.class, Integer.class, Boolean.class, Boolean.class, String.class};
        
        TabellenModell einModell = new TabellenModell(eineListe.size(), titel, canEdit, types);
        rechnungTBL.setModel(einModell);
        // </editor-fold>    
        
        while(einIterator.hasNext())
        {
            Rechnung eineRechnung = (Rechnung)einIterator.next();
            rechnungTBL.setValueAt(eineRechnung.getRechnungsNR(),i,0);
            rechnungTBL.setValueAt(eineRechnung.getKundenNr(),i,1);
            rechnungTBL.setValueAt(eineRechnung.getReservierungsNr(), i, 2);
            rechnungTBL.setValueAt(eineRechnung.getAnzahlMahnungen(), i , 3);
            rechnungTBL.setValueAt(eineRechnung.getIsOffen(),i, 4);
            rechnungTBL.setValueAt(eineRechnung.getEndabnahmeErfolg(),i, 5);
            rechnungTBL.setValueAt(eineRechnung.getRechnungsbetrag(),i, 6);
            
            i++;
        }
    }
    
