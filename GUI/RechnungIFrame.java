    private void bearbeitenBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bearbeitenBTNActionPerformed
        ListIterator einIterator = aenderungen.listIterator();
        RechnungsVerwaltung eineVerwaltung = new RechnungsVerwaltung();
        int rNr, spaltenNr;
        boolean isOffen, endabnahme;
        
        while(einIterator.hasNext())
        {
            spaltenNr = Integer.parseInt(einIterator.next().toString());
            if(spaltenNr!=-1)
            {
                rNr = Integer.parseInt(rechnungTBL.getValueAt(spaltenNr, 0).toString());
                isOffen = Boolean.parseBoolean(rechnungTBL.getValueAt(spaltenNr, 4).toString());
                endabnahme = Boolean.parseBoolean(rechnungTBL.getValueAt(spaltenNr, 5).toString());
                eineVerwaltung.update(rNr,isOffen,endabnahme);
            }
        }
    }//GEN-LAST:event_bearbeitenBTNActionPerformed

    private void rechnungTBLPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_rechnungTBLPropertyChange
        int spaltenNrTMP;
        boolean isVorhanden = false;
        try
        {   spaltenNrTMP = rechnungTBL.getSelectedRow();
            
            ListIterator einIterator = aenderungen.listIterator();
            while(einIterator.hasNext())
            {
                if(spaltenNrTMP == Integer.parseInt(einIterator.next().toString()))
                {
                    isVorhanden = true;
                }
            }
            
            if(!isVorhanden)
            {
                aenderungen.add(spaltenNrTMP);
            }
        }
        catch(Exception e){}
    }//GEN-LAST:event_rechnungTBLPropertyChange

